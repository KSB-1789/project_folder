import { createClient } from 'https://cdn.jsdelivr.net/npm/@supabase/supabase-js/+esm';

const supabaseUrl = window.SUPABASE_URL || 'YOUR_SUPABASE_URL';
const supabaseKey = window.SUPABASE_KEY || 'YOUR_SUPABASE_ANON_KEY';

if (!supabaseUrl || !supabaseKey) {
    console.error("Supabase credentials not found. Ensure netlify.js is generated by the build command.");
    const body = document.querySelector('body');
    if (body) {
        body.innerHTML = `<div style="font-family: sans-serif; padding: 2rem; text-align: center; color: red;"><h1>Configuration Error</h1><p>The application cannot connect to the backend. Please set up your Supabase credentials.</p></div>`;
    }
}

export const supabase = createClient(supabaseUrl, supabaseKey);